            class="relative bg-white dark:bg-gray-800 rounded-3xl shadow-2xl {{ $maxWidthClasses }} w-full max-h-[90vh] overflow-y-auto"
            @click.stop
        >
            <!-- Close button -->
            <button @click="open = false" class="absolute top-4 right-4 p-1 rounded-full hover:bg-gray-100 dark:hover:bg-gray-700 text-gray-400 hover:text-gray-600 z-10">
